package java_basics;

public class Variables {

	public static void main(String[] args) {
		int iMaxValue = Integer.MAX_VALUE;
		int iMinValue = Integer.MIN_VALUE;
		double dMaxValue = Double.MAX_VALUE;
		double dMinValue = Double.MIN_VALUE;
		String helloString = "Hello";
		System.out.println(iMaxValue);
		System.out.println(iMinValue);
		System.out.println(dMaxValue);
		System.out.println(dMinValue);
		System.out.println(helloString);
	}

}
